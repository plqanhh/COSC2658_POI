public class Point {
    private int x;
    private int y;

    // Constructor
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Getters
    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    // String representation for easy debugging
    @Override
    public String toString() {
        return "(" + x + ", " + y + ")";
    }
}
